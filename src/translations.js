/** Get translated question label, falling back to English then FORMS_DB label */
export function getLabel(lang, questionId, fallback) {
  if (lang === 'en') return fallback
  return LABELS[questionId]?.[lang] || fallback
}

/** Get translated section title, falling back to English then FORMS_DB title */
export function getSectionTitle(lang, sectionId, fallback) {
  if (lang === 'en') return fallback
  return SECTIONS[sectionId]?.[lang] || fallback
}

/**
 * Get translated option labels for a select question.
 * Returns an array aligned with question.options (English values).
 * English values are still stored in answers so FIELD_DRAW_MAP matching works.
 */
export function getOptionLabels(lang, questionId, englishOptions) {
  if (lang === 'en' || !englishOptions) return englishOptions
  const translated = OPTIONS[questionId]?.[lang]
  if (!translated || translated.length !== englishOptions.length) return englishOptions
  return translated
}
